def _parse_timeframe(value: str) -> TimeFrame:
    normalized = value.strip().lower()
    aliases = {
        "1min": TimeFrame.Minute,
        "5min": TimeFrame(5, TimeFrameUnit.Minute),
        "15min": TimeFrame(15, TimeFrameUnit.Minute),
        "30min": TimeFrame(30, TimeFrameUnit.Minute),
        "1h": TimeFrame.Hour,
        "1hour": TimeFrame.Hour,
        "1d": TimeFrame.Day,
        "1day": TimeFrame.Day,
    }
    if normalized in aliases:
        return aliases[normalized]

    raise HTTPException(status_code=400, detail=f"Unsupported timeframe: {value}")


def _calc_rsi(closes: list[float], period: int = 14) -> float | None:
    if len(closes) <= period:
        return None

    deltas = [closes[i] - closes[i - 1] for i in range(1, len(closes))]
    gains = [max(delta, 0.0) for delta in deltas]
    losses = [abs(min(delta, 0.0)) for delta in deltas]

    avg_gain = sum(gains[:period]) / period
    avg_loss = sum(losses[:period]) / period

    for i in range(period, len(deltas)):
        avg_gain = ((avg_gain * (period - 1)) + gains[i]) / period
        avg_loss = ((avg_loss * (period - 1)) + losses[i]) / period

    if avg_loss == 0:
        return 100.0

    rs = avg_gain / avg_loss
    return 100 - (100 / (1 + rs))


def _calc_rsi_series(closes: list[float], period: int = 14) -> list[float | None]:
    """Return RSI values aligned to closes; leading values are None until RSI is ready."""
    if not closes:
        return []
    if len(closes) <= period:
        return [None] * len(closes)

    deltas = [closes[i] - closes[i - 1] for i in range(1, len(closes))]
    gains = [max(delta, 0.0) for delta in deltas]
    losses = [abs(min(delta, 0.0)) for delta in deltas]

    avg_gain = sum(gains[:period]) / period
    avg_loss = sum(losses[:period]) / period

    out: list[float | None] = [None] * len(closes)
    out[period] = 100.0 if avg_loss == 0 else 100 - (100 / (1 + (avg_gain / avg_loss)))

    for i in range(period, len(deltas)):
        avg_gain = ((avg_gain * (period - 1)) + gains[i]) / period
        avg_loss = ((avg_loss * (period - 1)) + losses[i]) / period
        out[i + 1] = 100.0 if avg_loss == 0 else 100 - (100 / (1 + (avg_gain / avg_loss)))

    return out


def _calc_sma_series(values: list[float | None], period: int) -> list[float | None]:
    """Return SMA values aligned to input list; outputs None while warm-up is incomplete."""
    if period <= 0 or not values:
        return [None] * len(values)

    out: list[float | None] = [None] * len(values)
    for i in range(period - 1, len(values)):
        window = values[i - period + 1 : i + 1]
        if any(v is None for v in window):
            continue
        out[i] = float(sum(v for v in window if v is not None) / period)
    return out


