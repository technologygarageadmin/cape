@app.post("/api/ai-trade/stop")
def ai_trade_stop(body: AiTradeStopBody) -> dict[str, Any]:
    """
    Close the active AI trade position for the given symbol.
    Called when the user switches from AI mode to Manual mode.
    """
    ticker = body.symbol.strip().upper()

    try:
        response = trading_client.close_position(ticker)
    except Exception as ex:
        err_msg = str(ex).lower()
        # Position may already be closed or never existed — treat as graceful stop
        if any(k in err_msg for k in ("position does not exist", "404", "no position", "not found")):
            return {"message": "no_active_position", "symbol": ticker, "exit_price": None}
        raise HTTPException(
            status_code=502, detail=f"Failed to stop AI trade for {ticker}: {str(ex)}"
        ) from ex

    order = _serialize_order(response) if response is not None else None
    exit_price_raw = order.get("filled_avg_price", "") if order else ""
    try:
        exit_price = float(exit_price_raw) if exit_price_raw else None
    except (TypeError, ValueError):
        exit_price = None

    return {
        "message": "ai_trade_stopped",
        "symbol": ticker,
        "exit_price": exit_price,
        "order": order,
    }


