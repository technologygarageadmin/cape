@app.get("/api/options/suggest")
def suggest_options(
    symbol: str = Query(default=SYMBOL),
    option_type: str | None = Query(default=None, description="Override RSI signal: 'call' or 'put'"),
    strike_price: float | None = Query(default=None, description="Override strike anchor; finds nearest listed contract"),
) -> dict[str, Any]:
    """
    Suggest the best option contract for manual trading.
    Uses live price → OBR → RSI trend → select_best_contract from existing modules.
    All steps degrade gracefully so the endpoint always returns a usable suggestion.
    """
    ticker = symbol.strip().upper()
    warnings: list[str] = []

    # ── 1. Live price (required — fail fast only here) ──────────────────────
    try:
        quotes = _fetch_quotes_for_symbols([ticker])
        current_price = quotes[0]["price"] if quotes else 0.0
    except Exception as ex:
        raise HTTPException(status_code=502, detail=f"Failed to fetch live price: {str(ex)}") from ex

    if not current_price:
        raise HTTPException(status_code=502, detail=f"No live price available for {ticker}")

    # ── 2. Opening bar range (optional — may be unavailable outside market hours) ──
    obr_high: float | None = None
    obr_low: float | None = None
    try:
        obr_high, obr_low, _ = fetch_obr(stock_client, ticker)
    except Exception as ex:
        warnings.append(f"OBR unavailable: {str(ex)[:120]}")
        # Synthetic OBR: ±0.3 % around current price as placeholder
        obr_high = round(current_price * 1.003, 2)
        obr_low  = round(current_price * 0.997, 2)

    # ── 3. RSI analysis (optional — may fail when bars are stale/insufficient) ──
    rsi_result: dict[str, Any] = {}
    rsi_value: float | None = None
    try:
        rsi_result = analyze_rsi(ticker)
        rsi_value  = rsi_result.get("latest_rsi")
    except Exception as ex:
        warnings.append(f"RSI unavailable: {str(ex)[:120]}")

    # ── 4. Signal direction ──────────────────────────────────────────────────
    signal_name: str | None = None
    contract_type: ContractType | None = None

    if rsi_result:
        signal_name, contract_type, _, _ = determine_signal(
            rsi_result, current_price
        )

    if signal_name is None:
        # Fallback: use RSI base_trend if available, else assume CALL
        base_trend = rsi_result.get("base_trend", "UPTREND")
        if base_trend == "DOWNTREND":
            signal_name   = "PUT"
            contract_type = ContractType.PUT
        else:
            signal_name   = "CALL"
            contract_type = ContractType.CALL

    direction    = "uptrend"  if signal_name == "CALL" else "downtrend"
    resolved_opt = "call"     if signal_name == "CALL" else "put"

    # ── 4b. Honour manual overrides from the UI ──────────────────────────────
    # NOTE: use the *original query param* (option_type), not the local variable
    # (resolved_opt) that was just set from RSI — they share the same name so we
    # must check the function parameter before it could be shadowed.
    forced_from_ui = option_type  # this is still the Query() param here
    if forced_from_ui is not None:
        forced = forced_from_ui.strip().lower()
        if forced in ("call", "put"):
            signal_name   = forced.upper()
            contract_type = ContractType.CALL if forced == "call" else ContractType.PUT
            direction     = "uptrend" if forced == "call" else "downtrend"
            resolved_opt  = forced

    # Anchor for contract selection: prefer user-supplied strike, fall back to live price
    price_anchor = float(strike_price) if strike_price else current_price
    base_expiry = get_expiry_date(ny_trading_date())
    expiry_candidates = [
        base_expiry,
        base_expiry + timedelta(days=7),
        base_expiry + timedelta(days=14),
        base_expiry + timedelta(days=21),
    ]

    # ── 6. Best liquid contract — must come from Alpaca listed contracts ─────────────
    contract_name: str | None = None
    found_strike: float | None = None
    expiry = base_expiry
    last_err_msg = ""

    for exp in expiry_candidates:
        try:
            contract = select_best_contract(
                trading_client,
                option_data_client,
                ticker,
                exp,
                contract_type,
                price_anchor,          # use user's strike as anchor if provided
                min_option_volume=5,
                allow_low_volume_fallback=True,
                fallback_windows=[1.0, 2.5, 5.0],
            )
            found_strike = float(contract.strike_price)
            contract_name = contract.symbol
            expiry = exp
            break
        except Exception as ex:
            last_err_msg = str(ex)
            warnings.append(f"No listed contracts for expiry {exp}: {last_err_msg[:120]}")

    if contract_name is None:
        raise HTTPException(
            status_code=502,
            detail=(
                f"No listed Alpaca option contracts found for {ticker} in the next 4 expiries "
                f"({', '.join(str(e) for e in expiry_candidates)}). Last error: {last_err_msg[:220]}"
            ),
        )

    # ── 7. Bid / Ask for the selected contract ────────────────────────────────
    bid_price: float = 0.0
    ask_price: float = 0.0
    mid_price: float = 0.0
    spread_pct: float = 0.0
    try:
        snapshots = option_data_client.get_option_snapshot(
            build_option_snapshot_request([contract_name])
        )
        snap = extract_snapshot_for_symbol(snapshots, contract_name)
        bid_price, ask_price = _extract_option_bid_ask(snap)
        if bid_price > 0 and ask_price > 0:
            mid_price  = round((bid_price + ask_price) / 2, 4)
            spread_pct = round((ask_price - bid_price) / ask_price * 100, 2)
    except Exception as ex:
        warnings.append(f"Could not fetch bid/ask: {str(ex)[:80]}")

    return {
        "symbol":        ticker,
        "current_price": current_price,
        "strike_price":  found_strike,
        "direction":     direction,
        "option_type":   resolved_opt,
        "expiry":        str(expiry),
        "qty":           QTY,
        "contract_name": contract_name,
        "bid":           bid_price,
        "ask":           ask_price,
        "mid":           mid_price,
        "spread_pct":    spread_pct,
        "obr_high":      obr_high,
        "obr_low":       obr_low,
        "rsi":           rsi_value,
        "signal":        signal_name,
        "warnings":      warnings,
    }


