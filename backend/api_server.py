@app.post("/api/positions/{symbol}/close")
def close_position_endpoint(symbol: str) -> dict[str, Any]:
    ticker = symbol.strip().upper()
    position_obj = None
    entry_time = None
    buy_price = 0.0
    current_price = 0.0
    qty = 0
    side_raw = ""

    try:
        positions = trading_client.get_all_positions()
        for p in positions:
            if str(getattr(p, "symbol", "") or "").upper() == ticker:
                position_obj = p
                break
        if position_obj is not None:
            entry_time = _position_entry_time_map([position_obj]).get(ticker)
            buy_price = float(getattr(position_obj, "avg_entry_price", 0.0) or 0.0)
            current_price = float(getattr(position_obj, "current_price", 0.0) or 0.0)
            qty = int(abs(float(getattr(position_obj, "qty", 0) or 0)))
            side_raw = str(getattr(position_obj, "side", "") or "").lower()
    except Exception:
        position_obj = None

    result = None
    try:
        # If we detected a live position from the broker, prefer submitting
        # an explicit market order so the liquidation hits Alpaca immediately.
        if position_obj is not None and qty > 0:
            sell_side = OrderSide.BUY if "short" in side_raw else OrderSide.SELL

            # If the bot has an internal registry entry for this contract, capture it
            buy_order_id_for_contract = None
            try:
                for lot in get_open_positions():
                    if str(lot.get("contract_symbol") or "") == str(ticker):
                        buy_order_id_for_contract = lot.get("buy_order_id")
                        break
            except Exception:
                buy_order_id_for_contract = None

            # Cancel any open TP/SL bracket orders for this contract first.
            # Without this the market sell is rejected with "held_for_orders"
            # because the bracket's TP/SL children already commit the full qty.
            try:
                _open_orders = trading_client.get_orders(
                    filter=GetOrdersRequest(status=QueryOrderStatus.OPEN, symbols=[ticker])
                )
                _cancelled_any = False
                for _oo in _open_orders or []:
                    try:
                        trading_client.cancel_order_by_id(str(getattr(_oo, "id", "") or ""))
                        _cancelled_any = True
                    except Exception:
                        pass
                if _cancelled_any:
                    time.sleep(0.5)
            except Exception:
                pass

            # Submit a market order immediately (do not attach TP/SL here)
            try:
                order = place_market_order(trading_client, ticker, qty, sell_side, allow_limit=False)
                order_id = str(getattr(order, "id", "") or "")
                # If we have a registry buy_order_id, mark it as SELLING now
                if buy_order_id_for_contract and order_id:
                    try:
                        mark_selling(buy_order_id_for_contract, order_id)
                    except Exception:
                        pass

                # Wait briefly for fill to capture filled price for the log
                try:
                    filled = wait_for_fill(trading_client, order_id, FILL_WAIT_SEC)
                    result = _serialize_order(filled or order)
                    # If we have a registered lot, finalize it as closed
                    if buy_order_id_for_contract and getattr(filled or order, "status", None) and str(getattr(filled or order, "status", "")).lower() == "filled":
                        try:
                            close_position(buy_order_id_for_contract)
                        except Exception:
                            pass
                except Exception:
                    # Fill may not complete within timeout — still return submitted order
                    result = _serialize_order(order) if order is not None else None
            except Exception as ex:
                # If direct market order failed, fall back to the convenience close_position
                try:
                    response = trading_client.close_position(ticker)
                    result = _serialize_order(response) if response is not None else None
                except Exception:
                    result = None
        else:
            # No broker-side position detected — use client-close as a fallback
            try:
                response = trading_client.close_position(ticker)
                result = _serialize_order(response) if response is not None else None
            except Exception:
                result = None
    except Exception:
        result = None

    logged_trade = None
    if position_obj is not None and qty > 0:
        exit_reason = "MANUAL_LIQUIDATE"
        fill_raw = (result or {}).get("filled_avg_price", "")
        try:
            sell_price = float(fill_raw) if fill_raw else float(current_price or buy_price)
        except Exception:
            sell_price = float(current_price or buy_price)

        if "short" in side_raw:
            pnl = round((buy_price - sell_price) * qty * 100, 2)
            direction = "SHORT"
        else:
            pnl = round((sell_price - buy_price) * qty * 100, 2)
            direction = "LONG"
        result_label = "WIN" if pnl > 0 else "LOSS" if pnl < 0 else "BREAKEVEN"
        now_iso = datetime.now(CDT).isoformat(timespec="seconds")
        contract_name = ticker
        underlying_symbol = _underlying_from_option_symbol(ticker)

        option_type = "—"
        if len(ticker) >= 9:
            cp = ticker[-9:-8]
            if cp == "C":
                option_type = "call"
            elif cp == "P":
                option_type = "put"

        if _options_log_col is not None:
            _sl_base = -STOP_LOSS_PCT * 100
            _m_pnl_pct = round((float(sell_price) - float(buy_price)) / float(buy_price) * 100, 4) if float(buy_price) else None
            _snap = {
                "peak_pnl_pct": round(_m_pnl_pct or 0.0, 4),
                "exit_sl_pct":  round(max(_sl_base, (_m_pnl_pct or 0.0) + _sl_base), 4),
                "exit_qp_pct":  round(max(0.0, (_m_pnl_pct or 0.0) - QP_GAP_PCT) if (_m_pnl_pct or 0.0) > 0.0 else 0.0, 4),
                "exit_tp_pct":  round(TAKE_PROFIT_PCT * 100, 4),
            }
            _m_dur = None
            try:
                if entry_time and now_iso:
                    _m_dur = round((datetime.fromisoformat(now_iso) - datetime.fromisoformat(entry_time)).total_seconds())
            except Exception:
                pass
            _ml_doc = {
                "symbol": underlying_symbol,
                "name": underlying_symbol,
                "contract_name": contract_name,
                "direction": direction,
                "option_type": option_type,
                "strike_price": "—",
                "expiry": "—",
                "qty": qty,
                "buy_price": float(buy_price),
                "sell_price": float(sell_price),
                "pnl": float(pnl),
                "pnl_pct": _m_pnl_pct,
                "result": result_label,
                "exit_reason": exit_reason,
                "trade_type": "MANUAL_LIQUIDATE",
                "entry_signal_time": entry_time or "—",
                "entry_signal_price": float(buy_price),
                "buy_filled_time": entry_time or "—",
                "buy_filled_price": float(buy_price),
                "exit_signal_time": now_iso,
                "exit_signal_price": float(sell_price),
                "sell_filled_time": now_iso,
                "sell_filled_price": float(sell_price),
                "entry_time": entry_time or "—",
                "exit_time": now_iso,
                "created_at": datetime.now(CDT),
                "trade_duration_sec": _m_dur,
                "timeline": [],
                **_snap,
            }
            ins = _options_log_col.insert_one(_ml_doc)
            logged_trade = {
                "id": str(ins.inserted_id),
                "type": "manual",
                "symbol": _ml_doc["symbol"],
                "name": _ml_doc["name"],
                "contractName": _ml_doc["contract_name"],
                "strikePrice": _ml_doc["strike_price"],
                "optionType": _ml_doc["option_type"],
                "direction": _ml_doc["direction"],
                "expiry": _ml_doc["expiry"],
                "qty": _ml_doc["qty"],
                "buyPrice": _ml_doc["buy_price"],
                "sellPrice": _ml_doc["sell_price"],
                "pnl": _ml_doc["pnl"],
                "result": _ml_doc["result"],
                "entryTime": _ml_doc["entry_time"],
                "exitTime": _ml_doc["exit_time"],
                "exitReason": exit_reason,
            }

    return {
        "message": "position_close_requested",
        "symbol": ticker,
        "result": result,
        "exit_reason": "MANUAL_LIQUIDATE",
        "logged_trade": logged_trade,
    }


