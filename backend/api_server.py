_orders_cache: list[Any] = []
_orders_cache_ts: float = 0.0
_ORDERS_CACHE_TTL: float = 60.0  # seconds


def _get_cached_orders() -> list[Any]:
    """Return closed orders, refreshing at most once per minute."""
    global _orders_cache, _orders_cache_ts
    import time as _time
    if _time.monotonic() - _orders_cache_ts < _ORDERS_CACHE_TTL:
        return _orders_cache
    try:
        _orders_cache = trading_client.get_orders(
            filter=GetOrdersRequest(status=QueryOrderStatus.CLOSED, limit=500)
        )
        _orders_cache_ts = _time.monotonic()
    except Exception:
        pass  # Return stale cache on failure
    return _orders_cache


def _position_entry_time_map(positions: list[Any]) -> dict[str, str]:
    """Best-effort map: position symbol -> last fill time that opened current side."""
    if not positions:
        return {}

    try:
        orders = _get_cached_orders()
    except Exception:
        return {}

    by_symbol: dict[str, str] = {}
    for position in positions:
        symbol = str(getattr(position, "symbol", "") or "")
        if not symbol:
            continue

        side_raw = str(getattr(position, "side", "")).lower()
        open_side = "buy" if "long" in side_raw else "sell" if "short" in side_raw else None
        if not open_side:
            continue

        latest_fill = None
        for order in orders:
            order_symbol = str(getattr(order, "symbol", "") or "")
            order_side = str(getattr(getattr(order, "side", ""), "value", getattr(order, "side", ""))).lower()
            order_status = str(getattr(getattr(order, "status", ""), "value", getattr(order, "status", ""))).lower()
            filled_at = getattr(order, "filled_at", None)
            if order_symbol != symbol or order_side != open_side or order_status != "filled" or filled_at is None:
                continue
            if latest_fill is None or filled_at > latest_fill:
                latest_fill = filled_at

        if latest_fill is not None:
            by_symbol[symbol] = _to_iso(latest_fill) or str(latest_fill)

    return by_symbol


