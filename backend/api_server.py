@app.get("/api/bars")
def get_bars(
    symbol: str = Query(default=SYMBOL),
    timeframe: str = Query(default="1Min"),
    limit: int = Query(default=200, ge=5, le=1000),
    since: str | None = Query(default=None),
) -> dict[str, Any]:
    ticker = symbol.upper().strip()
    tf = _parse_timeframe(timeframe)
    end = datetime.now(timezone.utc)

    since_dt: datetime | None = None

    if since:
        try:
            since_dt = datetime.fromisoformat(since.replace("Z", "+00:00"))
            if since_dt.tzinfo is None:
                since_dt = since_dt.replace(tzinfo=timezone.utc)
            # Fetch a 2-hour lookback before `since` so RSI has enough history.
            # This is far cheaper than fetching 7 days, but ensures RSI_PERIOD bars.
            start = since_dt - timedelta(hours=2)
        except Exception:
            since_dt = None

    if since_dt is None:
        # Alpaca returns bars oldest-first; passing `limit` cuts the window at the
        # START (oldest) side, meaning recent bars get dropped when the window spans
        # more trading sessions than `limit` can cover.  Fix: request without a
        # `limit`, fetch the full window, then slice the TAIL to get the most-recent
        # `limit` bars.
        #
        # Window sizing: ensure the window holds at least `limit` worth of bars.
        #   1m/800 bars → 800min ÷ 390min/day ≈ 2.1 trading days → 7 cal days
        #   5m/200 bars → 1000min ÷ 78bars/day ≈ 2.6 trading days → 7 cal days
        #   Others: 7 cal days is comfortably enough.
        timeframe_key = str(timeframe or "").lower()
        if "day" in timeframe_key or timeframe_key in {"1d", "day", "d"}:
            days_back = max(365, int(limit * 2.2))
        elif "hour" in timeframe_key or timeframe_key in {"1h", "4h"}:
            days_back = max(30, int(limit / 6))
        else:
            days_back = 7
        start = end - timedelta(days=days_back)

    try:
        response = stock_client.get_stock_bars(
            _build_stock_bars_request(
                symbol_or_symbols=[ticker],
                timeframe=tf,
                start=start,
                end=end,
                # No `limit` here — we slice the tail ourselves so we always
                # get the MOST RECENT bars, not the oldest ones.
            )
        )
        bars = extract_bars_for_symbol(response, ticker)
    except Exception as ex:
        raise HTTPException(status_code=502, detail=f"Failed to fetch bars: {str(ex)}") from ex

    if not bars:
        raise HTTPException(status_code=404, detail=f"No bars found for symbol {ticker}")

    # Keep full bars for RSI calculation, then slice return rows.
    # For incremental polls (since_dt set): RSI is calculated over the full 2-hour
    # lookback window but only bars newer than since_dt are returned to the client.
    all_bars = list(bars)
    if since_dt is not None:
        bars = [b for b in all_bars if getattr(b, "timestamp", None) and getattr(b, "timestamp") > since_dt]
        bars = bars[-limit:]
    else:
        bars = all_bars[-limit:]
    since_iso = _to_iso(since_dt) if since_dt is not None else None
    start_idx = max(0, len(all_bars) - len(bars)) if since_dt is None else 0

    all_closes = [float(getattr(bar, "close", 0.0) or 0.0) for bar in all_bars]
    all_timestamps = [_to_iso(getattr(bar, "timestamp", None)) for bar in all_bars]

    rsi_series = _calc_rsi_series(all_closes, period=RSI_PERIOD)
    rsi_ma_series = _calc_sma_series(rsi_series, period=RSI_MA_PERIOD)

    rsi_points: list[dict[str, Any]] = []
    rsi_ma_points: list[dict[str, Any]] = []
    for i in range(start_idx, len(all_bars)):
        ts = all_timestamps[i]
        if ts is None:
            continue
        # For incremental polls, only return RSI points newer than `since` so the
        # frontend can append them rather than replacing the full series.
        if since_iso is not None and ts <= since_iso:
            continue
        rv = rsi_series[i]
        mv = rsi_ma_series[i]
        if rv is not None:
            rsi_points.append({"timestamp": ts, "value": round(float(rv), 6)})
        if mv is not None:
            rsi_ma_points.append({"timestamp": ts, "value": round(float(mv), 6)})

    rsi_markers: list[dict[str, Any]] = []
    seen_marker_keys: set[tuple[str, str]] = set()
    for i in range(max(2, start_idx), len(all_bars)):
        ts = all_timestamps[i]
        if ts is None:
            continue
        if since_iso is not None and ts <= since_iso:
            continue

        r2, r1, r0 = rsi_series[i - 2], rsi_series[i - 1], rsi_series[i]
        m2, m1, m0 = rsi_ma_series[i - 2], rsi_ma_series[i - 1], rsi_ma_series[i]
        if None in (r2, r1, r0, m2, m1, m0):
            continue

        cross_up_now = bool(r1 <= m1 and r0 > m0)
        cross_down_now = bool(r1 >= m1 and r0 < m0)
        prev_cross_up = bool(r2 <= m2 and r1 > m1)
        prev_cross_down = bool(r2 >= m2 and r1 < m1)

        if ENTRY_ALLOW_PREV_BAR_CROSS:
            cross_up_signal = cross_up_now or prev_cross_up
            cross_down_signal = cross_down_now or prev_cross_down
        else:
            cross_up_signal = cross_up_now
            cross_down_signal = cross_down_now

        rsi_gap_ok_up = (r0 - m0) >= MIN_RSI_MA_GAP
        rsi_gap_ok_down = (m0 - r0) >= MIN_RSI_MA_GAP

        if cross_up_signal and rsi_gap_ok_up:
            marker_ts = ts
            if ENTRY_ALLOW_PREV_BAR_CROSS and (not cross_up_now) and prev_cross_up:
                marker_ts = all_timestamps[i - 1] or ts
            key = (marker_ts, "CALL")
            if marker_ts and key not in seen_marker_keys:
                seen_marker_keys.add(key)
                rsi_markers.append({"timestamp": marker_ts, "signal": "CALL", "direction": "up"})
        elif cross_down_signal and rsi_gap_ok_down:
            marker_ts = ts
            if ENTRY_ALLOW_PREV_BAR_CROSS and (not cross_down_now) and prev_cross_down:
                marker_ts = all_timestamps[i - 1] or ts
            key = (marker_ts, "PUT")
            if marker_ts and key not in seen_marker_keys:
                seen_marker_keys.add(key)
                rsi_markers.append({"timestamp": marker_ts, "signal": "PUT", "direction": "down"})

    rows = []
    closes: list[float] = []
    for bar in bars:
        close = float(getattr(bar, "close", 0.0) or 0.0)
        closes.append(close)
        rows.append(
            {
                "timestamp": _to_iso(getattr(bar, "timestamp", None)),
                "open": float(getattr(bar, "open", 0.0) or 0.0),
                "high": float(getattr(bar, "high", 0.0) or 0.0),
                "low": float(getattr(bar, "low", 0.0) or 0.0),
                "close": close,
                "volume": int(getattr(bar, "volume", 0) or 0),
                "trade_count": int(getattr(bar, "trade_count", 0) or 0),
                "vwap": float(getattr(bar, "vwap", 0.0) or 0.0),
            }
        )

    obr_payload: dict[str, Any] | None = None
    try:
        obr_high, obr_low, obr_bartime = fetch_obr(stock_client, ticker)
        obr_payload = {
            "high": obr_high,
            "low": obr_low,
            "bar_time": obr_bartime,
        }
    except Exception:
        obr_payload = None

    return {
        "symbol": ticker,
        "timeframe": timeframe,
        "count": len(rows),
        "rsi": next((float(v) for v in reversed(rsi_series) if v is not None), None),
        "rsi_period": RSI_PERIOD,
        "rsi_ma_period": RSI_MA_PERIOD,
        "rsi_points": rsi_points,
        "rsi_ma_points": rsi_ma_points,
        "rsi_markers": rsi_markers,
        "obr": obr_payload,
        "bars": rows,
    }


def _fetch_quotes_for_symbols(symbols: list[str]) -> list[dict[str, Any]]:
    try:
        trades_resp = stock_client.get_stock_latest_trade(_build_latest_trade_request(symbols))
    except Exception:
        trades_resp = {}

    quotes_resp = None
    try:
        quotes_resp = stock_client.get_stock_latest_quote(_build_latest_quote_request(symbols))
    except Exception:
        quotes_resp = None

    payload: list[dict[str, Any]] = []
    for symbol in symbols:
        trade = _extract_by_symbol(trades_resp, symbol)
        quote = _extract_by_symbol(quotes_resp, symbol) if quotes_resp is not None else None

        trade_price = float(getattr(trade, "price", 0.0) or 0.0)
        bid_price = float(getattr(quote, "bid_price", 0.0) or 0.0) if quote else 0.0
        ask_price = float(getattr(quote, "ask_price", 0.0) or 0.0) if quote else 0.0
        midpoint = (bid_price + ask_price) / 2 if bid_price and ask_price else 0.0

        payload.append(
            {
                "symbol": symbol,
                "price": trade_price or midpoint,
                "bid": bid_price if quote else None,
                "ask": ask_price if quote else None,
                "bid_size": int(getattr(quote, "bid_size", 0) or 0) if quote else None,
                "ask_size": int(getattr(quote, "ask_size", 0) or 0) if quote else None,
                "trade_timestamp": _to_iso(getattr(trade, "timestamp", None)),
                "quote_timestamp": _to_iso(getattr(quote, "timestamp", None)) if quote else None,
            }
        )

    return payload


