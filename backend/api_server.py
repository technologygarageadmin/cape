def _is_mkt_open_now() -> bool:
    """True if current ET time is within regular trading hours (Mon–Fri 09:30–16:00)."""
    now_et = datetime.now(ET)
    if now_et.weekday() >= 5:          # Saturday / Sunday
        return False
    mins = now_et.hour * 60 + now_et.minute
    return (9 * 60 + 30) <= mins < (16 * 60)


async def _run_straddle_for_symbol(symbol: str) -> None:
    """Compute CALL + PUT straddle legs for one symbol and upsert into MongoDB."""
    loop = asyncio.get_event_loop()
    try:
        # 1. Live price
        quotes = _fetch_quotes_for_symbols([symbol])
        if not quotes or not quotes[0].get("price"):
            print(f"[straddle] {symbol}: no price, skipping")
            return
        current_price = float(quotes[0]["price"])

        # 2. Expiry (next Friday)
        expiry = get_expiry_date()

        # 3. CALL contract
        call_strike, call_name = None, None
        try:
            cc = await loop.run_in_executor(
                None,
                lambda: select_best_contract(
                    trading_client, option_data_client,
                    symbol, expiry, ContractType.CALL, current_price,
                ),
            )
            call_strike = float(cc.strike_price)
            call_name   = cc.symbol
        except Exception as ex:
            call_strike = round(current_price + 1.0, 2)
            print(f"[straddle] {symbol} CALL fallback strike {call_strike}: {ex}")

        # 4. PUT contract
        put_strike, put_name = None, None
        try:
            pc = await loop.run_in_executor(
                None,
                lambda: select_best_contract(
                    trading_client, option_data_client,
                    symbol, expiry, ContractType.PUT, current_price,
                ),
            )
            put_strike = float(pc.strike_price)
            put_name   = pc.symbol
        except Exception as ex:
            put_strike = round(current_price - 1.0, 2)
            print(f"[straddle] {symbol} PUT fallback strike {put_strike}: {ex}")

        # 5. OBR
        obr_high, obr_low = None, None
        try:
            obr_high, obr_low, _ = await loop.run_in_executor(
                None, lambda: fetch_obr(stock_client, symbol)
            )
        except Exception:
            pass

        # 6. RSI
        rsi_val = None
        try:
            rsi_result = await loop.run_in_executor(None, lambda: analyze_rsi(symbol))
            rsi_val = float(
                getattr(rsi_result, "rsi", None)
                or getattr(rsi_result, "value", None)
                or 0
            ) or None
        except Exception:
            pass

        # 7. Upsert into MongoDB (one record per symbol per calendar date)
        now_cdt = datetime.now(CDT)
        today   = now_cdt.strftime("%Y-%m-%d")
        doc = {
            "symbol":        symbol,
            "date":          today,
            "entry_price":   current_price,
            "expiry":        str(expiry),
            "call_strike":   call_strike,
            "put_strike":    put_strike,
            "call_contract": call_name,
            "put_contract":  put_name,
            "obr_high":      obr_high,
            "obr_low":       obr_low,
            "rsi":           rsi_val,
            "status":        "OPEN",
            "entry_time":    now_cdt.isoformat(timespec="seconds"),
            "exit_time":     None,
            "high_price":    current_price,
            "low_price":     current_price,
            "current_price": current_price,
            "move_pct":      0.0,
        }
        if _straddle_col is not None:
            _straddle_col.update_one(
                {"symbol": symbol, "date": today},
                {"$setOnInsert": doc},
                upsert=True,
            )
            print(f"[straddle] {symbol} stored — CALL ${call_strike} / PUT ${put_strike}")
    except Exception as ex:
        print(f"[straddle] {symbol} unexpected error: {ex}")


async def _straddle_runner_loop() -> None:
    """
    Background loop that fires at each market open:
    • Waits until 09:30 ET (or immediately if market already open with no records today).
    • Records straddle entries only for symbols enabled in WATCHLIST_SYMBOLS.
    • After running, sleeps until the next trading-day open.
    """
    await asyncio.sleep(15)            # brief delay so server is fully up

    while True:
        if _straddle_col is None:
            await asyncio.sleep(3_600)
            continue

        today = datetime.now(CDT).strftime("%Y-%m-%d")
        enabled_symbols = [sym for sym, is_enabled in WATCHLIST_SYMBOLS.items() if is_enabled]

        if _is_mkt_open_now():
            if not enabled_symbols:
                print("[straddle] No symbols enabled in WATCHLIST_SYMBOLS — skipping straddle run")
                await asyncio.sleep(8 * 3_600)
                continue

            existing = _straddle_col.count_documents(
                {"date": today, "symbol": {"$in": enabled_symbols}}
            )
            if existing < len(enabled_symbols):
                print(f"[straddle] Market open — recording straddles for {len(enabled_symbols)} enabled symbols")
                existing_rows = {
                    row.get("symbol")
                    for row in _straddle_col.find(
                        {"date": today, "symbol": {"$in": enabled_symbols}},
                        {"symbol": 1},
                    )
                }
                for sym in enabled_symbols:
                    if sym in existing_rows:
                        continue
                    await _run_straddle_for_symbol(sym)
                    await asyncio.sleep(2)     # throttle Alpaca API calls
                print("[straddle] All symbols recorded.")
            else:
                print(f"[straddle] Already recorded today ({existing} symbols) — skipping")
            # Sleep well past market close, then re-check tomorrow
            await asyncio.sleep(8 * 3_600)
        else:
            # Not open yet — poll every 60 s
            await asyncio.sleep(60)


async def _straddle_monitor_loop() -> None:
    """
    Background loop that runs every 60 s while the market is open:
    • Re-fetches live prices for today's OPEN straddles.
    • Updates high_price, low_price, move_pct.
    • Sets status → WIN_UP / WIN_DOWN when the stock moves ≥ STRADDLE_TP_PCT.
    """
    await asyncio.sleep(60)           # wait for runner to seed first records

    while True:
        await asyncio.sleep(60)
        if _straddle_col is None or not _is_mkt_open_now():
            continue
        try:
            today        = datetime.now(CDT).strftime("%Y-%m-%d")
            open_strads  = list(_straddle_col.find({"date": today, "status": "OPEN"}))
            if not open_strads:
                continue

            syms   = list({s["symbol"] for s in open_strads})
            quotes = {
                q["symbol"]: float(q["price"])
                for q in _fetch_quotes_for_symbols(syms)
                if q.get("price")
            }

            for strad in open_strads:
                sym   = strad["symbol"]
                price = quotes.get(sym)
                if price is None:
                    continue
                entry    = float(strad["entry_price"])
                move_pct = round(abs(price - entry) / entry * 100, 2)
                new_high = max(float(strad.get("high_price", entry)), price)
                new_low  = min(float(strad.get("low_price",  entry)), price)

                new_status = (
                    "WIN_UP"   if move_pct >= _STRADDLE_TP_PCT * 100 and price > entry
                    else "WIN_DOWN" if move_pct >= _STRADDLE_TP_PCT * 100 and price < entry
                    else "OPEN"
                )
                _straddle_col.update_one(
                    {"_id": strad["_id"]},
                    {"$set": {
                        "current_price": price,
                        "high_price":    new_high,
                        "low_price":     new_low,
                        "move_pct":      move_pct,
                        "status":        new_status,
                    }},
                )
        except Exception as ex:
            print(f"[straddle monitor] error: {ex}")


