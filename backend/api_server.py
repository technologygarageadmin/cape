@app.post("/api/orders")
def create_order(body: CreateOrderBody) -> dict[str, Any]:
    side_value = body.side.strip().lower()
    tif_value = body.time_in_force.strip().lower()

    side_map = {
        "buy": OrderSide.BUY,
        "sell": OrderSide.SELL,
    }
    tif_map = {
        "day": TimeInForce.DAY,
        "gtc": TimeInForce.GTC,
        "ioc": TimeInForce.IOC,
        "fok": TimeInForce.FOK,
    }

    if side_value not in side_map:
        raise HTTPException(status_code=400, detail="Invalid side. Use buy or sell.")

    if tif_value not in tif_map:
        raise HTTPException(status_code=400, detail="Invalid time_in_force.")

    try:
        order = trading_client.submit_order(
            MarketOrderRequest(
                symbol=body.symbol.strip().upper(),
                qty=body.qty,
                side=side_map[side_value],
                time_in_force=tif_map[tif_value],
            )
        )
    except Exception as ex:
        raise HTTPException(status_code=502, detail=f"Failed to place order: {str(ex)}") from ex

    return {
        "message": "order_submitted",
        "order": _serialize_order(order),
    }


